package com.StaticVoidGames.spring.util;

public class OpenSourceLink {

	private String title;
	private String url;
	
	public OpenSourceLink(String title, String url) {
		this.title = title;
		this.url = url;
	}
	
	public String getTitle() {
		return title;
	}
	public String getUrl() {
		return url;
	}
}
